class VehicleRegistrationModel {
  String numberIn = "";
  String provinceIn = "";
  String numberOut = "";
  String provinceOut = "";

  String get getNumberIn => numberIn;
  void setNumberIn(s) {
    numberIn = s;
  }

  String get getProvinceIn => provinceIn;
  void setProvinceIn(s) {
    provinceIn = s;
  }

  String get getNumberOut => numberOut;
  void setNumberOut(s) {
    numberOut = s;
  }

  String get getProvinceOut => provinceOut;
  void setProvinceOut(s) {
    provinceOut = s;
  }
}
